CREATE TABLE IF NOT EXISTS optim_results (
    match_id VARCHAR(50),
    sport_key VARCHAR(50),
    game VARCHAR(100),
    date_match DATE,
    time_match TIME,
    home_team VARCHAR(100),
    away_team VARCHAR(100),
    model VARCHAR(50),
    datetime_inference TIMESTAMP,
    prob_home_win FLOAT,
    prob_draw FLOAT,
    prob_away_win FLOAT,
    odds_home FLOAT,
    odds_draw FLOAT,
    odds_away FLOAT,
    bookmaker_home VARCHAR(100),
    bookmaker_draw VARCHAR(100),
    bookmaker_away VARCHAR(100),
    f_home_kelly FLOAT,
    f_draw_kelly FLOAT,
    f_away_kelly FLOAT,
    datetime_optim TIMESTAMP,
    PRIMARY KEY (match_id, model, datetime_inference, datetime_optim)
);